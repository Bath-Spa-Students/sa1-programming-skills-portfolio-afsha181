#using increment of 1
print("counting up from 0 to 50:")
i = 0
while i <= 50:
    print(i, end=", ")
    i += 1
print('''
''')


#using increment of 1
print("counting down from 50 to 0 :")
i = 50
while i >= 0:
    print(i, end=", ")
    i -= 1
print('''
''')


